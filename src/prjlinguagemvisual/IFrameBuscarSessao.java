    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int i = cbTipoSala.getSelectedIndex();
        
        if (i>0) {
            pesquisa();
        }
        else{
            carregarAtributosTabela();
            
        }
        if (lenSessao<tbSessao.getRowCount()) {
            int temp = tbSessao.getRowCount()-lenSessao;
            lenSessao = tbSessao.getRowCount();
            JOptionPane.showMessageDialog(rootPane,"Atualizado com sucesso!\n"+Integer.toString(temp)+""
                    + " novo (s) iten(s)");
            }
        else{
            JOptionPane.showMessageDialog(rootPane, "Atualizado com sucesso!\nNem um novo registro encontado.");
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void cbTipoSalaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbTipoSalaActionPerformed
        // TODO add your handling code here:
        int i = cbTipoSala.getSelectedIndex();
        
        if (i>0) {
            cbSala.removeAllItems();
            try {
            SalaDAO salaDAO = new SalaDAO();
            salaDAO.show(cbTipoSala.getItemAt(i)).stream().forEach((Sala s)->{
                cbSala.addItem(s);
            
            });
            } catch (SQLException e) {
                System.err.println("erro: "+e);
            }
        }
       
    }//GEN-LAST:event_cbTipoSalaActionPerformed

