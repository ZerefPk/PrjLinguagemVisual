    public void carregarAtributosTabela(){
        cbSala.removeAllItems();
        limparAtrinbutosTabela();
        DefaultTableModel val = (DefaultTableModel) tbSessao.getModel();
        try {
            SessaoDAO model = new SessaoDAO();
            model.show().stream().forEach((Sessao s)->{
                val.addRow(new Object[]{
                    s.getCodFilme().getNomeFilme(),
                    s.getDataSessao(),
                    s.getHoraSessao(),
                    (s.getCodsala().getNumeroSala()+" - "+s.getCodsala().getTipoSala())
                
                });
            });
        } catch (SQLException e) {
        }
    }
    
    public void limparAtrinbutosTabela(){
        ((DefaultTableModel) tbSessao.getModel()).setRowCount(0);
    }
    public void pesquisa(){
        
        limparAtrinbutosTabela();
        
        int sala = cbSala.getSelectedIndex();
              
        DefaultTableModel val = (DefaultTableModel) tbSessao.getModel();
        try {
            SessaoDAO model = new SessaoDAO();
            model.showBusca(cbSala.getItemAt(sala)).stream().forEach((Sessao s)->{
                val.addRow(new Object[]{
                    s.getCodFilme().getNomeFilme(),
                    s.getDataSessao(),
                    s.getHoraSessao(),
                    (s.getCodsala().getNumeroSala()+" - "+s.getCodsala().getTipoSala())
                
                });
            });
        } catch (SQLException e) {
            System.err.println("erro cod: "+e);
        }
        if (tbSessao.getRowCount()==0) {
              
            JOptionPane.showMessageDialog(rootPane, "Nem um registro encontado");
            
            cbSala.removeAllItems();
            cbTipoSala.setSelectedIndex(0);
            carregarAtributosTabela();
        }
        
    }
