    public void carregarAtributosTabela(){
        DefaultTableModel val = (DefaultTableModel) tbFilmes.getModel();
        try {
            FilmeDAO filmes = new FilmeDAO();
            filmes.showFilmes().stream().forEach((Filme f)->{
                val.addRow(new Object[]{
                    f.getId(),
                    f.getNomeFilme(),
                    f.getAnoLancamento(),
                    f.getCategoria().getNomeCategoria(),
                    f.getDireitor().getNomeDiretor()
                    
                });
                
                
            });
        } catch (SQLException ex) {
            System.err.println("erro cod: "+ex);
        }
    }
    public void limparAtrinbutosTabela(){
        ((DefaultTableModel) tbFilmes.getModel()).setRowCount(0);
    }
    public void pesquisa(){
        limparAtrinbutosTabela();
        DefaultTableModel val = (DefaultTableModel) tbFilmes.getModel();

        
        try {
            FilmeDAO filmes = new FilmeDAO();
            filmes.showFilmesBusca(txtPesquisa.getText()).stream().forEach((Filme f)->{
               val.addRow(new Object[]{
                    f.getId(),
                    f.getNomeFilme(),
                    f.getAnoLancamento(),
                    f.getCategoria().getNomeCategoria(),
                    f.getDireitor().getNomeDiretor()
                    
                });
                
                
            });
            if (tbFilmes.getRowCount()==0) {
                
                JOptionPane.showMessageDialog(null, "NÃ£o existe Filme com esse Titulo");
                carregarAtributosTabela();
                txtPesquisa.setText(null);
            }
            
        } catch (SQLException ex) {
            System.err.println("erro cod: "+ex);
        }
    }

