    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
        // TODO add your handling code here:
        int i = txtPesquisa.getText().length();
       
        if(i>0){
            pesquisa();
            
        }
        else{
            limparAtrinbutosTabela();
            carregarAtributosTabela();
           
        }
        if (len<tbFilmes.getRowCount()) {
            int temp = tbFilmes.getRowCount();
            JOptionPane.showMessageDialog(rootPane,"Atualizado com sucesso!\n"+Integer.toString(temp)+""
                    + " novo (s) iten(s)");
            
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Atualizado com sucesso!\nNem um novo registro encontado.");
        }
        
    }//GEN-LAST:event_btnAtualizarActionPerformed

